
public interface Figurainterface {
    // Un método que, cuando se implemente, calculará y devolverá el área de una
    // figura.
    public float CalculaArea();

    // Un método que, cuando se implemente, calculará y devolverá el volumen de una
    // figura.
    public float CalculaVolumen();

    // Un método que, cuando se implemente, calculará y devolverá el perímetro de
    // una figura.
    public float CalculaPerimetro();

    // Un método que, cuando se implemente, devolverá el nombre de una figura.
    public String ObtenerNombre();

    // Un método que, cuando se implemente, devolverá información sobre las
    // instancias de una figura.
    public String ObtenerInstancias();

    // Otro método que, cuando se implemente, devolverá información sobre las
    // instancias de una figura.
    public String ObtenerInstancias2();
}
